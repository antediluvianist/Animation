    static int currentFrame = 0;
    static System.Timers.Timer animationTimer;

    static void Main()
    {
        // Configurer le Timer pour l'animation
        animationTimer = new System.Timers.Timer(150); // Intervalle de 500 ms
        animationTimer.Elapsed += OnTimedEvent;
        animationTimer.AutoReset = true;
        animationTimer.Enabled = true;

        // Initialiser la console
        Console.CursorVisible = false; // Masquer le curseur pour réduire le clignotement

        // Empêcher le programme de se terminer immédiatement
        Console.ReadLine();
    }

    private static void OnTimedEvent(Object source, System.Timers.ElapsedEventArgs e)
    {
        // Sauvegarder la position actuelle du curseur
        int cursorLeft = Console.CursorLeft;
        int cursorTop = Console.CursorTop;

        // Positionner le curseur au début de la console
        Console.SetCursorPosition(0, 0);

        // Afficher la frame actuelle
        Console.WriteLine(frames[currentFrame]);

        // Restaurer la position du curseur
        Console.SetCursorPosition(cursorLeft, cursorTop);

        // Passer à la frame suivante
        currentFrame = (currentFrame + 1) % frames.Length;
    }
